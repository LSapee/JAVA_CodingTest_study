package silver;

public class Stu {
    private int age;
    private String name;

    public Stu(int a, String s) {
        this.age = a;
        this.name =s;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
